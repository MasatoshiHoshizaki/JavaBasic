public class PTra02_01 {
	public static void main(String[] args) {

		// ★ 20を5で割った値を出力してください
		int divid;
		divid = 20 / 5;
		System.out.println(divid);

		// ★ 126に13を掛けた値を出力してください
		int times;
		times = 126 * 13;
		System.out.println(times);

		// ★ 97を6で割ったときの余りを出力してください
		int amount;
		amount = 97 % 6;
		System.out.println(amount);

	}
}
